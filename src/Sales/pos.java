    private void txtpcodeKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtpcodeKeyPressed
        // TODO add your handling code here:
        
        //Only Executes when pressed enter
        if(evt.getKeyCode()== KeyEvent.VK_ENTER) {
            
            //Records the text everytime it is changed
            String pcode = txtpcode.getText();
            
            try {
                Class.forName("com.mysql.jdbc.Driver");
                con = DriverManager.getConnection("jdbc:mysql://localhost/salespos", "root", ""); //connection url
                ps = con.prepareStatement("select * from product where id = ?"); //query statement
                ps.setString(1, pcode);
                rs = ps.executeQuery();
                
                if(rs.next()== false)
                {
                    JOptionPane.showMessageDialog(this, "Product Code not found");
                    
                } else {
                    
                    //Getting from the database
                    String pname = rs.getString("prodname");
                    String price = rs.getString("price");
                    
                    //Setting to the widgets
                    
                    txtpname.setText(pname.trim());
                    txtprice.setText(price.trim());
                    
                }
                
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(pos.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(pos.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            
            
        }
        
        
        
        
        
    }//GEN-LAST:event_txtpcodeKeyPressed
    
