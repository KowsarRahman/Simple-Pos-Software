    public void Balance() {
        
        
        int total = Integer.parseInt(txttotall.getText());
        int pay = Integer.parseInt(txtamount.getText());
        int bal = pay - total;
        txtbal.setText(String.valueOf(bal));
        
        
    }
    
    public void bill() {
        
        String total = txttotall.getText();
        String pay = txtamount.getText();
        String balance = txtbal.getText();
        DefaultTableModel model = new DefaultTableModel();
        model = (DefaultTableModel)jTable1.getModel();
        
        txtbill.setText(txtbill.getText() + "***************************\n");
        txtbill.setText(txtbill.getText() + "********POST BILL*********\n");
        txtbill.setText(txtbill.getText() + "***************************\n");
        
        //Heading
        txtbill.setText(txtbill.getText() + "Product" + "\t" + "Price" + "\t" +"Amount" + "\n");
        
        for(int i =0; i < model.getRowCount(); i++) {
            
            String pname = (String)model.getValueAt(i, 1);
            String price = (String)model.getValueAt(i, 3);
            String amount = (String)model.getValueAt(i, 4);
            
            
            txtbill.setText(txtbill.getText() + pname + "\t" + price + "\t" + amount + "\n");
            
            
            
            
        }
        
        txtbill.setText(txtbill.getText() + "Subtotal: " + total + "\n");
        txtbill.setText(txtbill.getText() + "Pay: " + pay + "\n");
        txtbill.setText(txtbill.getText() + "Change: " + balance + "\n");
        txtbill.setText(txtbill.getText() + "*********BYTE36 Software**********\n");
        
        
        
    }
